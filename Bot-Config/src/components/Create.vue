
<template>
  <page-content page-title="Create Bot">
    <h2> Configuration </h2>
    <md-input-container md-clearable>
      <label>Name</label>
      <md-input v-model="botName"></md-input>
    </md-input-container>

    <app-tree :tree="tree"></app-tree>

    <md-button @click="post" class="md-raised md-primary">Submit</md-button>
  </page-content>
</template>




<script>
import PageContent from '@/components/layout/PageContent'
import Input from '@/components/Input'
import Tree from '@/components/Tree'

export default {
  name: 'Create',
  components: {
    'page-content': PageContent,
    'input-field': Input,
    'app-tree': Tree
  },
  data () {
    return {
      botName: 'test',
      template: '',
      tree: {}
    }
  },
  methods: {
    post () {
      let url = `http://localhost:3000/api/v1/manage/bot`
      let headers = new Headers({ 'Content-Type': 'application/json' })
      console.log(this.botName)
      let payload = JSON.stringify({
        name: this.botName,
        template: 'Welcome Bot'
      })
      console.log(payload)
      let request = new Request(url, {
        method: 'POST',
        mode: 'CORS',
        headers: headers,
        body: payload
      })

      fetch(request).then(response => {
        if (response.ok) {
          this.$router.push({name: 'Overview'})
        } else { console.log(response) }
      })
        .catch(error => console.log(error.message))
    }
  }
}
</script>

